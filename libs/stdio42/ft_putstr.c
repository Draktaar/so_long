#include "stdio42.h"

int	ft_putstr(char *str, int fd)
{
	int	i;

	i = 0;
	if (str == 0)
		return (ft_putstr("(null)", fd));
	while (str[i])
		i += ft_putchar(str[i], fd);
	return (i);
}
