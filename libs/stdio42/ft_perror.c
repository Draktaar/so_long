#include "stdio42.h"

void	ft_perror(const char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	write(STDERR_FILENO, &*s, i);
	write(STDERR_FILENO, "\n", 1);
}
